# Assignment Operators

c = 10 - 3 # Subtraction operator
print(c) # Output: 7

d = 5 * 4 # Multiplication operator

d -= 5 # Subtraction assignment operator, equivalent to d = d - 5
print(d) # Output: 2

c += 2 # Addition assignment operator, equivalent to c = c + 2
print(c) # Output: 9


c/= 2 # Division assignment operator, equivalent to c = c / 2
print(c) # Output: 4.5


c *= 3 # Multiplication assignment operator, equivalent to c = c * 3
print(c) # Output: 13.5



# Comparison Operators

c ==d # Equal to operator, returns True if c is equal to d, otherwise returns False
print(c == d) # Output: False


c != d # Not equal to operator, returns True if c is not equal to d, otherwise returns False
print(c != d) # Output: True  

c > d # Greater than operator, returns True if c is greater than d, otherwise returns False
print(c > d) # Output: True

c < d # Less than operator, returns True if c is less than d, otherwise returns False
print(c < d) # Output: False

c >= d # Greater than or equal to operator, returns True if c is greater than or equal to d, otherwise returns False
print(c >= d) # Output: True

c <= d # Less than or equal to operator, returns True if c is less than or equal to d, otherwise returns False
print(c <= d) # Output: False


# Logical Operators

x = True
y = False

print(x and y) # Logical AND operator, returns True if both x and y are True, otherwise returns False
print(x or y) # Logical OR operator, returns True if either x or y is True
print(not x) # Logical NOT operator, returns True if x is False, otherwise returns False


print ("True or False is " + str(x or y)) # Output: True or False is False
print ("True and False is " + str(x and y)) # Output: True and False is False
print ("Not True is " + str(not x)) # Output: Not True is False
print("False or False is " + str(y or y)) # Output: False or False is False
print ("Not True is " + str(not x)) # Output: Not True is False


