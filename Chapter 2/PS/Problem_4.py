# Use a comparison operator to compare the following pairs of numbers and print the result: take a = 34 and b = 80

a = 34

b = 80

print(a > b) # Output: False, because 34 is not greater than 80
print(a < b) # Output: True, because 34 is less than 80
print(a == b) # Output: False, because 34 is not equal to 80
print(a != b) # Output: True, because 34 is not equal to 80
print(a >= b) # Output: False, because 34 is not greater than or equal
print(a <= b) # Output: True, because 34 is less than or equal to 80


