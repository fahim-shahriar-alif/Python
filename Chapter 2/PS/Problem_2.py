# Write a python code to find reminder when a number is divided by z


a = 10

z = 3

reminder = a % z # The modulus operator (%) returns the remainder of the division of a by z

print("The reminder when " + str(a) + " is divided by " + str(z) + " is " + str(reminder)) # Output: The reminder when 10 is divided by 3 is 1
